package com.example.shstore.data

/**
 * Синглтон для хранения данных текущей сессии пользователя в памяти.
 * Данные профиля кэшируются здесь, чтобы при повторном входе
 * на экран профиля не было пустых полей до загрузки с сервера.
 *
 * Дата создания: 2025
 * Автор: ShStore Team
 */
object UserSession {

    // ── Auth ──────────────────────────────────────────────────────────────────

    /** UUID пользователя из auth.users */
    var userId: String? = null

    /** JWT access_token Supabase */
    var accessToken: String? = null

    /** Email пользователя */
    var email: String? = null

    // ── Кэш профиля ───────────────────────────────────────────────────────────
    // Сохраняется при успешном сохранении на сервере.
    // При открытии экрана профиля поля заполняются отсюда мгновенно,
    // а затем обновляются с сервера в фоне.

    /** Имя */
    var firstName: String = ""

    /** Фамилия */
    var lastName: String = ""

    /** Адрес */
    var address: String = ""

    /** Телефон */
    var phone: String = ""

    /** Локальный URI сделанного фото (сохраняется между переходами) */
    var avatarUriString: String? = null
}